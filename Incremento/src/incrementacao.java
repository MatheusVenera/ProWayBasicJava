public class incrementacao {

	public static void main(String[] args) {

		//crescente
		for (int vezes = 1; vezes <= 10; vezes++) {
			System.out.print(vezes + " ");
		}
		
		System.out.println(" ");
		
		//descrescente
		for (int vezes = 10; vezes >= 1; vezes--) {
			System.out.print(vezes + " ");
		}

	}
}
